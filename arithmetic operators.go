package main

import "fmt"

func main() {
	p := 34
	q := 20
	//addition
	result1 := p + q
	fmt.Println("result of p+q :=", result1)

	//subtraction
	result2 := p - q
	fmt.Println("result of p-q :=", result2)

	//multiplication
	result3 := p * q
	fmt.Println("result of p*q :=", result3)

	//Division
	result4 := p / q
	fmt.Println("result of p/q :=", result4)

	//Modulus
	result5 := p % q
	fmt.Println("result of p%q :=", result5)

}
